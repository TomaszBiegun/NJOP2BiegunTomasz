    private void zliczMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_zliczMousePressed
        if (czy) {
            pole.setText("");
            Set set = hmap.entrySet();
            Iterator iterator = set.iterator();
            while (iterator.hasNext()) {
                Map.Entry mapka = (Map.Entry) iterator.next();
                pole.setText(pole.getText() + mapka.getKey() + " : " + mapka.getValue() + " wystąpień" + "\n");
                System.out.print("key is: " + mapka.getKey() + " & Value is: ");
                System.out.println(mapka.getValue());
            }
        } else {
            pole.setText("Nie załadowałeś pliku");
        }

    }//GEN-LAST:event_zliczMousePressed

