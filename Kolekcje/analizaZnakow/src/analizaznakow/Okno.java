    private void otworzMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_otworzMousePressed
        czy = false;
        JFileChooser fc = new JFileChooser();
        String linia = "";
        fc.showOpenDialog(otworz);
        File plik = fc.getSelectedFile();
        String path = plik.getAbsolutePath();
        int count = 0;

        int liczbaLinii = 0;
        try {
            czy = true;
            pole.setText("");
            BufferedReader br = new BufferedReader(new FileReader(path));
            while ((linia = br.readLine()) != null) {
                liczbaLinii++;
            }
            br.close();
            BufferedReader br2 = new BufferedReader(new FileReader(path));
            System.out.println(liczbaLinii);
            String[] tab = new String[liczbaLinii];
            for (int i = 0; i < liczbaLinii; i++) {
                tab[i] = br2.readLine();
                for (int j = 0; j < tab[i].length(); j++) {
                    if (!hmap.containsKey(tab[i].charAt(j))) {
                        System.out.println(tab[i].charAt(j));
                        hmap.put(tab[i].charAt(j), 1);
                    } else {
                        count = hmap.get(tab[i].charAt(j));
                        hmap.put(tab[i].charAt(j), count + 1);
                    }
                }

            }

            for (int i = 0; i < tab.length; i++) {
                pole.setText(pole.getText() + tab[i] + "\n");
            }
            br2.close();

        } catch (Exception e) {
            e.printStackTrace();

        }

    }//GEN-LAST:event_otworzMousePressed

